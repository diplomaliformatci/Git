package Pattern;

public class Human {
	int weight;
	int height;
	int age;
	public Human(int weight , int height , int age){
		this.weight = weight;
		this.height = height;
		this.age =age;
	}
	
	public int getWeight(){
		return this.weight;
	}
	public int getAge(){
		return this.age;
	}
	public int getHeight(){
		return this.height
				;
	}
}
